           <div id="text">
                
                <p class="lead">Finding an accommodation will probably be the first thing that you will need to do before, or after you came to Bratislava and one of the hardest one. <span class="jedan">Brat</span><span class="dva">Go</span> will try to get you trough this obstacle. First thing you need to know is that the accommodation is expensive, especially if you are young or coming from outside EU.</p>
                
                <p class="lead">- If you want to rent a single room for one person in a large flat, be prepared to spend between 250€ to 320€ utilities included, depending on location, number of people in an apartment, public transportation lines, bars…</p>
                
               <p class="lead"> - Prices for one room apartment are around 450€ to 500€ per month, utilities included.</p>
                
               <p class="lead"> - One and a half room apartment will cost you 550€ to 600€ per month, utilities included.</p>
                
                <p class="lead">- Two rooms apartment will cost you 650€ to 700€ per month, utilities included.</p>
                
                <p class="lead">- Three rooms apartment will cost you… You get the point.</p>
                
               <p class="lead"> Surely you can find more favorable options, but you will need to look for them. Here, <span class="jedan">Brat</span><span class="dva">Go</span> will provide        you links for Facebook groups and internet websites where you can find an apartment to rent. </p> 
