            <h1 id="a"> Public transportation </h1>
            
            <p class="lead"> Public transportation in Bratislava consists of buses, trolley buses and trams. There is no metro system here, but don’t worry, you will be able to move around the city without it. </p>
            
            <p class="lead"> In this section, <span class="jedan">Brat</span><span class="dva">Go</span> will tell you what webpages or applications to use to easily move around the city, also, <span class="jedan">Brat</span><span class="dva">Go</span> will explain to you where and how to buy a ticket for public transportation or how to get a monthly ticket for transportation. So, let’s start. </p>
            
            <p class="lead"> To get around the city, <span class="jedan">Brat</span><span class="dva">Go</span> will recommend you to use a<a href="https://play.google.com/store/apps/details?id=com.google.android.apps.maps&hl=en_US"> Google maps</a>  application, because it combines a bus/trolley/tram lines together with a walking distance when switching lines. You can also use it for looking up a grocery stores, bars, pubs, streets near you.</p>
            
            <p class="lead">You can buy single use tickets for Public transportation system (MHD – Mestská Hromadná Doprava) on ticket sales devices, that are located on almost every bus, trolley or tram stop. You can pay with the coins or with debit/credit card. </p>
             
            <p class="lead">Prices for single use cards are shown in the table below. Tickets prices are determined on their time of duration. For short distances you can buy 15 minutes ticket, for longer distances you can buy 30 minutes ticket and so on. After you buy a ticket, you need to validate your ticket inside a bus/trolley/tram on a validation device. After that, you can switch as many buses/trolleys/trams lines as you want for a duration of the ticket. You can validate your ticket only once. Not validated ticket is invalid and you risk to pay a fine.  </p>
  
