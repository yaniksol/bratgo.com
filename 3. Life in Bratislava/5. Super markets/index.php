            <h1 id="a"> Supermarkets </h1>
            
            <p class="lead"> Maybe, you have never been in Slovakia and you are interested to know, what are the prices here in the Supermarkets… Well… Some things are cheap, others are expensive, depending on what are you looking for. Here, <span class="jedan">Brat</span><span class="dva">Go</span>  will tell you all about the prices for some basic things you will probably need. So, let’s start.  </p>
                
            <p class="lead"> Large supermarkets here, in Slovakia are <a href="https://tesco.sk/letak-strany/340402/hypermarkety/tesco-letak/"> Tesco </a>, <a href="https://www.kaufland.sk/aktualna-ponuka/letak.html"> Kaufland </a>, <a href="https://www.billa.sk/akcie/hlavny-letak"> Billa </a>,<a href="https://www.lidl.sk/o-nas/informacie/online-letak"> Lidl </a> and <a href="https://www.metro.sk/aktualna-ponuka/aktualne-letaky"> Metro </a> and you will probably use one of these for your weekly grocery shopping. On the other hand, walking on the street, you will see grocery stores such as <a href="https://www.terno.sk/aktualna-akciova-ponuka/"> Terno </a>, <a href="https://www.coop.sk/"> COOP </a>, <a href="https://deliapotraviny.sk/"> Delia </a> and <a href="http://malina.sk/"> Malina </a>. You will probably use these for your dally needs, or when you forgot to buy something in your weekly grocery shopping. By clicking on the names of the stores above, you will be able to see weakly catalogs.</p> 
            
            <p class="lead"> Be aware, Supermarkets in Slovakia are closed on state holidays and most of them closes between 21:00h – 22:00h. Only few of them are open during the night. </p>
                
