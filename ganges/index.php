            <h1>O nama</h1>
       <p class="lead">Ganges d.o.o. Doboj osnovan je 1996 godine sa ciljem plasmana roba industrijske potrošnje. </p>
        
            <p class="lead">Preduzeće posluje sa željeznicom, rudnicima, termo i hidro elektranama, komunalnim organizacijama i većim proizvodnim sistemima. S obzirom da su naši kupci pretežno firme sa učešćem državnog kapitala, naš promet se obavlja putem tendera. Pripremu tenderskih ponuda Ganges obavlja jako brzo i efikasno i u tom segmentu posjeduje zavidno znanje i iskustvo. Važi kao kvalitetan i pouzdan dobavljač. Zahvaljukući tome, ima uhodane komercijalne odnose sa firmama iz BiH. Posjeduje dobar, stručan i iskusan kadar i dobar informativni sistem. Stalno prati događanja na tržištu i tako planira svoj nastup. Od svog nastanka firma je uvjek bila likvidna, bez ibzira na turbulentna vremena koja su nailazila. I u takvim okolnostima osnovna misija je bila opstati na tržištu uprkos teškim uslovima i sačuvati imidž i pripremati se za nove izazove. Pripremajući se za nastzpajuća vremena privatizacije i odustajanja od nabavki regulisanih tenderima, Ganges će pokušati da obezbjedi jedan broj firmi koje će zastupati na tržištu BiH ili preuzeti distribuciju njihovih roba. To će nametnuti malo drugačiju logiku poslovanja, kojoj će se fleksibilna ekipa Gangesa vrlo brzo prilagoditi. Imamo u vidu stvaranje jeftinije i efikasnije distributivne mreže, sa svojim poddistributerima na cijeloj teritoriji BiH. U prilog nam ide dobra pozicioniranost firme, pošto Doboj leži u centralnom dijelu Bosne i Hercegovine, na raskrsnici magistralnih puteva i glavno je željeznično čvorište. </p>
